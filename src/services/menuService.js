export const menuService = {
  // Get all menu categories
  async getCategories() {
    try {
      const response = await api.get(ENDPOINTS.MENU.CATEGORIES);
      return response.data;
    } catch (error) {
      console.error('Get categories error:', error);
      // Return mock data if API fails
      return mockCategories;
    }
  },

  // Get menu items with filtering and pagination
  async getItems(filters = {}) {
    try {
      const params = {
        category: filters.category,
        search: filters.searchTerm,
        sort: filters.sortBy,
        page: filters.page || 1,
        limit: filters.limit || 20,
        ...filters,
      };

      const response = await api.get(ENDPOINTS.MENU.ITEMS, { params });
      return response.data;
    } catch (error) {
      console.error('Get items error:', error);
      // Return mock data if API fails
      let filteredItems = [...mockMenuItems];
      
      if (filters.category) {
        filteredItems = filteredItems.filter(item => item.category === filters.category);
      }
      
      if (filters.searchTerm) {
        const searchTerm = filters.searchTerm.toLowerCase();
        filteredItems = filteredItems.filter(item => 
          item.name.toLowerCase().includes(searchTerm) ||
          item.description.toLowerCase().includes(searchTerm) ||
          item.tags.some(tag => tag.toLowerCase().includes(searchTerm))
        );
      }

      const page = filters.page || 1;
      const limit = filters.limit || 20;
      const startIndex = (page - 1) * limit;
      const endIndex = startIndex + limit;
      const paginatedItems = filteredItems.slice(startIndex, endIndex);

      return {
        items: paginatedItems,
        total: filteredItems.length,
        page: page,
        limit: limit,
        totalPages: Math.ceil(filteredItems.length / limit)
      };
    }
  },

  // Get item details
  async getItemDetails(itemId) {
    try {
      const response = await api.get(buildUrl(`${ENDPOINTS.MENU.ITEM_DETAILS}/:id`, { id: itemId }));
      return response.data;
    } catch (error) {
      console.error('Get item details error:', error);
      // Find item in mock data
      const item = mockMenuItems.find(item => item.id === parseInt(itemId));
      if (item) return item;
      throw new Error('Item not found');
    }
  },

  // Search menu items
  async searchItems(query, filters = {}) {
    try {
      const params = {
        q: query,
        ...filters,
      };

      const response = await api.get(ENDPOINTS.MENU.SEARCH, { params });
      return response.data;
    } catch (error) {
      console.error('Search items error:', error);
      // Search in mock data
      const searchTerm = query.toLowerCase();
      const results = mockMenuItems.filter(item => 
        item.name.toLowerCase().includes(searchTerm) ||
        item.description.toLowerCase().includes(searchTerm) ||
        item.tags.some(tag => tag.toLowerCase().includes(searchTerm))
      );
      
      return {
        items: results,
        total: results.length,
        page: 1,
        limit: results.length
      };
    }
  },

  // Get personalized recommendations
  async getRecommendations(limit = 10) {
    try {
      const params = { limit };
      const response = await api.get(ENDPOINTS.MENU.RECOMMENDATIONS, { params });
      return response.data;
    } catch (error) {
      console.error('Get recommendations error:', error);
      // Return popular items as recommendations
      const popularItems = mockMenuItems.filter(item => item.popular).slice(0, limit);
      return popularItems.length > 0 ? popularItems : mockMenuItems.slice(0, limit);
    }
  },

  // Get item customizations
  async getCustomizations(itemId) {
    try {
      const response = await api.get(buildUrl(`${ENDPOINTS.MENU.CUSTOMIZATIONS}/:id`, { id: itemId }));
      return response.data;
    } catch (error) {
      console.error('Get customizations error:', error);
      // Return default customizations
      return {
        id: itemId,
        options: [
          {
            name: "Spice Level",
            options: ["Mild", "Medium", "Hot", "Extra Hot"],
            default: "Medium"
          },
          {
            name: "Sauce",
            options: ["Original", "BBQ", "Honey Mustard", "Ranch"],
            default: "Original"
          },
          {
            name: "Sides",
            options: ["Fries", "Rice", "Salad", "Mashed Potatoes"],
            default: "Fries"
          }
        ]
      };
    }
  },

  // Check item availability
  async checkAvailability(itemId, quantity = 1) {
    try {
      const response = await api.get(`/menu/items/${itemId}/availability`, {
        params: { quantity },
      });
      return response.data;
    } catch (error) {
      console.error('Check availability error:', error);
      // Return mock availability
      const item = mockMenuItems.find(item => item.id === parseInt(itemId));
      return {
        available: item ? item.available : false,
        maxQuantity: item ? 50 : 0,
        estimatedWaitTime: "15-20 minutes"
      };
    }
  },

  // Get popular items
  async getPopularItems(limit = 8) {
    try {
      const response = await api.get('/menu/popular', { params: { limit } });
      return response.data;
    } catch (error) {
      console.error('Get popular items error:', error);
      // Return mock popular items if API fails
      return mockMenuItems.filter(item => item.popular).slice(0, limit);
    }
  },

  // Cache menu data
  cacheMenuData(category, data) {
    const cacheKey = `menu_${category}`;
    storageService.set(cacheKey, data, 5 * 60 * 1000); // 5 minutes cache
  },

  // Get cached menu data
  getCachedMenuData(category) {
    const cacheKey = `menu_${category}`;
    return storageService.get(cacheKey);
  },
};